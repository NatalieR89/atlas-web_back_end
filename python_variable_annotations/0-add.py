#!/usr/bin/env python3
"""
A function that adds two floats and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
